#include "printf.h"

int	ft_varstr_s(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
		j++;
	}
	return (j);
}
